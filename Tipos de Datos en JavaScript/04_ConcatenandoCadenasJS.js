var nombre = "Alex";
var apellido ="Martinez";

//Por variables
var NombreCompleto = nombre + " " + apellido;
console.log(NombreCompleto);

//Por string
var NombreCompleto2 = 'Cris' + ' ' + ' Martinez';
console.log(NombreCompleto2);

