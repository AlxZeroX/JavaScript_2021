let a =3, b=2;
let z= a+b;

//Incremento
//Pre-incremento (Operador ++ antes de la variable)
z= ++a;

console.log(a);
console.log(z);

//Post-Incremento (el operador ++ despues de la variable)
z=b++;
console.log(b);
console.log(z);

//Decremento
//Pre-Decremento
z=--a;
console.log(a);
console.log(z);

//Post-Decremento
z=a--;
console.log(a);
console.log(z);

